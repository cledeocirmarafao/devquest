//se eu colocar um dos valores entre aspas("4") ele vai concatenar os valores e ficará 42; acontece assim se tentarmos somar valores que não são do mesmo tipo
let primeiroValor = 4
let segundoValor = 2

//somando
primeiroValor + segundoValor
console.log(primeiroValor + segundoValor)//6

//subtraindo
console.log(primeiroValor - segundoValor)//2

//dividindo
console.log(primeiroValor  / segundoValor)//2

//multiplicando
console.log(primeiroValor * segundoValor)//8

//Resto da divisão
console.log(primeiroValor % segundoValor)//0